//substractTwoNumbers
export function substractTwoNumbers(num1 = 0 , num2 = 0) {
  return num1 - num2;
}

//timesTwoNumbers
export function timesTwoNumbers(num1 = 0 , num2 = 0) {
  return num1 * num2;
}

//divideTwoNumbers 
export function divideTwoNumbers (num1 = 0 , num2 = 0) {
  return num1 / num2;
}

